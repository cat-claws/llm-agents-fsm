#!/usr/bin/env bash
# ============================================================
#  git-learning-lab  —  self-contained installer / resetter
#  Mirrors the curriculum of learngitbranching.js.org
#
#  Usage:
#    bash git-learning-installer.sh          # install into current directory
#    bash git-learning-installer.sh reset    # wipe & restore snapshot in current directory
#    bash git-learning-installer.sh status   # show current directory state
#
#  Optional:
#    GIT_LEARNING_INSTALL_DIR=/path bash git-learning-installer.sh install
# ============================================================
set -euo pipefail

INSTALL_DIR="${GIT_LEARNING_INSTALL_DIR:-$PWD}"
INSTALL_DIR="$(cd "$INSTALL_DIR" && pwd)"
LAB="${INSTALL_DIR}/git-learning-lab"
REMOTE="${INSTALL_DIR}/git-learning-remote.git"
SCRIPT_PATH="$0"
SCRIPT_NAME="$(basename "$SCRIPT_PATH")"

# ── helpers ─────────────────────────────────────────────────
red()   { printf '\033[0;31m%s\033[0m\n' "$*"; }
green() { printf '\033[0;32m%s\033[0m\n' "$*"; }
bold()  { printf '\033[1m%s\033[0m\n' "$*"; }
hr()    { printf '%0.s─' $(seq 1 60); echo; }

confirm() {
    printf '\033[0;33m%s\033[0m [y/N] ' "$1"
    read -r ans
    [[ "$ans" =~ ^[Yy]$ ]]
}

# ── extract snapshot ────────────────────────────────────────
extract_snapshot() {
    local tmptar
    tmptar="$(mktemp /tmp/git-learning-snap.XXXXXX.tar.gz)"

    # The snapshot is base64-encoded below the __PAYLOAD__ marker
    awk '/^__PAYLOAD__/{found=1; next} found{print}' "$0"         | base64 --decode > "$tmptar"

    tar xzf "$tmptar" -C "$INSTALL_DIR"
    rm -f "$tmptar"

    # Fix the remote URL inside the lab repo to match the selected install path
    git -C "$LAB" remote set-url origin "$REMOTE"
    git -C "$LAB" config user.email "learner@example.com"
    git -C "$LAB" config user.name  "Git Learner"
}

# ── install ─────────────────────────────────────────────────
cmd_install() {
    if [[ -d "$LAB" || -d "$REMOTE" ]]; then
        red "Already installed in: $INSTALL_DIR"
        echo "Run from that directory:  bash $SCRIPT_PATH reset"
        exit 1
    fi
    bold "Installing git-learning-lab into:"
    echo "  $INSTALL_DIR"
    extract_snapshot
    green "Done! Open $LAB/EXERCISES.md to begin."
    hr
    git -C "$LAB" log --oneline --graph --all | head -30
}

# ── reset ────────────────────────────────────────────────────
cmd_reset() {
    bold "This will DESTROY all local changes in:"
    echo "  $LAB"
    echo "  $REMOTE"
    confirm "Reset to factory snapshot?" || { echo "Aborted."; exit 0; }

    bold "Resetting..."
    rm -rf "$LAB" "$REMOTE"
    extract_snapshot
    green "Reset complete — back to the original snapshot."
    hr
    git -C "$LAB" log --oneline --graph --all | head -30
}

# ── status ───────────────────────────────────────────────────
cmd_status() {
    if [[ ! -d "$LAB" ]]; then
        red "No git-learning-lab found in: $INSTALL_DIR"
        echo "Run from the parent directory where you installed it."
        exit 1
    fi

    bold "=== git-learning-lab status ==="
    echo "Install directory: $INSTALL_DIR"
    echo ""
    bold "Branches:"
    git -C "$LAB" branch -a -vv
    echo ""
    bold "Tags:"
    git -C "$LAB" tag
    echo ""
    bold "Full graph:"
    git -C "$LAB" log --oneline --graph --all
}

# ── dispatch ────────────────────────────────────────────────
CMD="${1:-install}"
case "$CMD" in
    install) cmd_install ;;
    reset)   cmd_reset   ;;
    status)  cmd_status  ;;
    *)
        echo "Usage: bash $SCRIPT_PATH [install|reset|status]"
        echo "Installs into the current directory by default."
        echo "Optional: GIT_LEARNING_INSTALL_DIR=/path bash $SCRIPT_NAME install"
        exit 1
        ;;
esac

exit 0
